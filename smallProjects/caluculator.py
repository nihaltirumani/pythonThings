answer=input("what calculation want to do? ")

if answer=="addition":
    num1=int(input("first number "))
    num2=int(input("second number "))
    print(num1+num2)

elif answer=="subtraction":
    num1=int(input("first number "))
    num2=int(input("second number "))
    print(num1-num2)

elif answer=="multiplication":
    num1=int(input("first number "))
    num2=int(input("second number "))
    print(num1*num2)

elif answer=="division":
    num1=int(input("first number "))
    num2=int(input("second number "))
    print(num1/num2)
